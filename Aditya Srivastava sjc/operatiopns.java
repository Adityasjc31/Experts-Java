class operatiopns
{
    public static void main(String args [])
    {
        int a=5,b=10;
        int s=a+b;
        int d=a-b;
        int p=a*b;
        double q=a/(double)b;
        System.out.println(a+"+"+b+"="+s);
        System.out.println(a+"-"+b+"="+d);
        System.out.println(a+"*"+b+"="+p);
        System.out.println(a+"/"+b+"="+q);
    }
}